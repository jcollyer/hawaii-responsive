                <h3 class="hs1">Day 1, Arrivals</h3>
                <img src="assets/beach.jpg" class="pic1 right" />
                <p class="ps1">After landing at Honolulu International airport and collecting your
baggage, a Sea Hawaii staff member will meet you. From there you will be whisked
away on a super shuttle to the tropical paradise of Waikiki where you will enjoy a sun
filled 8 day trip of education and fun. After arriving at the hotel you will receive a
traditional Hawaiian, fresh, <a href="http://en.wikipedia.org/wiki/Lei_%28garland%29"> single orchid Lei </a>
greeting. A staff member will review your contact info and departure flight info and
check you in. From there you will sign out your room key and have the opportunity to
drop off your belongings, meet your roommates, and clean up from your journey. As
groups of students arrive, a tour of <a href="http://www.gohawaii.com/oahu/regions-
neighborhoods/honolulu/waikiki">Waikiki</a>  will be given by a certified Sea Hawaii guide to show
you the locations of our <a href="http://shorebirdwaikiki.com/breakfast/">breakfast buffet</a>, the
<a href="http://www.aloha-hawaii.com/oahu/beaches/waikiki-beaches/">beach</a>, banks, grocery
store, lunch options, shopping, and transportation staging areas. Traveling in the required
groups of 3, students will then be released for free time with a dinner voucher for a
restaurant TBD. Once all students have arrived there will be a mandatory meeting with
all tour guides, security staff, and students to review the rules and go over the following
day's activities. Room check will follow immediately.</p>
                <h3 class="hs1">Day 2, Island Tour</h3>
                <img src="assets/beach2.jpg" class="pic1 right" />
                <p class="ps1 clearfix">After eating a group breakfast at the hotel, students will assemble to get on the tour bus in a
luxurious  <a href="http://royalstarhawaii.com/index.php?option=com_content&task=view&id=12&Itemid=27">Royal Star Hawaii Motorcoach</a> for their tour around the island of
Oahu. A running commentary by a Sea Hawaii tour guide will be given on the tour.
Topics covered include: the formation of the 8 Hawaiian Islands and Loihi, the newest
forming island, Hawaiian culture, history, traditions, language, customs, geology, nature
conservation, environmental issues, and role in US military history. Additionally, we
will discuss Hawaii's Polynesian settlement, British arrival, unification, Euro-American
and Asian immigrators, the overthrow of the Hawaiian monarchy, a brief period as the
Republic of Hawaii, and admission to the US as Hawaii Territory and then as the state of
Hawaii. But don't worry there will be plenty of time for photo opportunities like out first
stop at the <a href="http://www.lotsafunmaps.com/gallery.php?id=72">Diamond Head Beach Park overlook </a>. From there we will drive through the affluent <a href="http://en.wikipedia.org/wiki/K%C4%81hala,_Hawaii">Kahala</a> neighborhood which houses the likes of Oprah
Winfrey, Pierce Brosnan, Bette Midler, Tom Selleck, Keanu Reeves, Liza Minelli, Burt
Reynolds and Dwayne "The Rock" Johnson. After Kahala we continue up the
Kalanianaole Highway for a stop at the <a href="http://en.wikipedia.org/wiki/Halona_Blowhole">Halona Blowhole </a>. Not only will there be picture opportunities of the Blowhole from
here, but also <a href="http://www.aloha.com/~lifeguards/sandy.html">Sandy Beach</a> and the
beach where the famous kissing scene in <a href="http://www.youtube.com/watch?v=1W6AGM-LxGY">"From Here To Eternity"</a> was shot. Be sure to ask your host
parents about that one! After that stop we wind our way through the island until we
reach our picnic destination on <a href="http://en.wikipedia.org/wiki/Lanikai_Beach">Lanikai Beach</a>.
in <a href="http://en.wikipedia.org/wiki/Kailua,_Honolulu_County,_Hawaii">Kailua</a>. Here you can swim, play soccer and volleyball,
throw the football or Frisbee, take a walk down the beach, or just eat, take a nap, and
catch some rays. Once we finish there we will hop back on the King Kamehameha
Highway and head toward the world famous waves of Oahu's  <a href="http://www.gohawaii.com/oahu/regions-neighborhoods/north-shore">North Shore</a>. Depending on the waves
we may stop at any one of the three most famous spots:  <a href="http://www.northshore.com/hawaii/sunset-beach-hawaii.asp">Sunset Beach</a>,  
<a href="http://www.northshore.com/hawaii/pipeline-beach-hawaii.asp">Banzai Pipeline</a>, or 
<a href="http://www.northshore.com/hawaii/waimea-bay-beach-hawaii.asp">Waimea Bay</a>. After checking out the
surfers catching some bombs we will make our way to <a href="http://www.gohawaii.com/oahu/regions-neighborhoods/north-shore/haleiwa">Haliewa</a>, 
a small surfer town and one of our favorites. While in Haliewa you can get a famous <a href="http://en.wikipedia.org/wiki/Shave_ice">Hawaiian shave ice</a> 
at <a href="http://www.matsumotoshaveice.com/">Matsumotos</a>  or <a href="http://www.aokishaveice.com/Aokis/index.html">Aokis</a>, a sandwich at 
<a herf="http://www.kua-aina.co.uk/">Kua 'Aina Burger</a>, or shop
amongst the many local shops and street vendors. You might be ready for a nap at this
point and if so that's ok because we will hop back on the motor coach and make our way
back across the middle of the island towards Waikiki for dinner. Along the way we will
also make a stop at the <a href="http://www.dole-plantation.com/">Dole Pineapple Plantation</a> for
(you guessed it!) more snacks and shopping. Dinner will be at a restaurant TBD.</p>
