                <h3 class="hs1">Who will meet me at the airport?</h3>
                <p class="ps1">Sea Hawaii Tours staff will meet you at the airport as soon as you collect your bags. We
will then meet you at <span class="ps2">Baggage Claim H</span> to coordinate your transfer to the hotel. Tour
staff will also assist in your shuttle departure back to the airport for your return flight.
