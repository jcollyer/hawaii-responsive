arrange your ticket to depart between 8am and 6pm. These specific time windows will
allow you to get the most out of your Hawaii experience and help us get you safely to and
from the airport with our shuttle company.</p>
                <h3 class="hs1">What happens if I miss my flight or it gets cancelled?</h3>
                <p class="ps1">If the airline cancels your flight and it's their fault, they must provide you with another
flight. They will also be responsible for any expenses you may incur as a result of the
cancellation. Save all receipts.
If your plane is delayed due to weather, the airline does not have to do anything other
than offer you a seat on a later flight. The airline will not pay for any extra costs you
may incur for hotels, taxicabs, food, etc.
If you miss your plane and it is your fault, the airline may charge you extra to rebook
it. You will be responsible for paying any additional costs yourself. Avoid this by
scheduling a reliable ride to the airport and give yourself plenty of time to check baggage
and get through airport security.</p>
                <h3 class="hs1">What flight information does Sea Hawaii Tours need from me after I book my ticket?</h3>
                 <p class="ps1">Sea Hawaii Tours will need ALL of your flight numbers.  This includes
ALL connections arriving and departing.  We also require an airline
conformation code, ticket number, and estimated arrival and departure
times.  If we haven't received this information from you 4 weeks
before the tour begins, we may not be able to let you participate in
the tour.</p>
